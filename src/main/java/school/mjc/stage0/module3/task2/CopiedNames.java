package school.mjc.stage0.module3.task2;

public class CopiedNames {
    public static void main(String[] args) {
        int initialVariable = 0; // Renamed "variable"
        int anotherVariable = 2000; // Renamed "variable"

        int dataValue = 1000;  // Renamed "value"

        int yetAnotherVariable = 4000; // Renamed "variable"
        int secondValue = 3000;  // Renamed "value2"

        int allVariables = 5000;  // Renamed "variables"
        allVariables = 7000;

        int updatedValue = 90000; // Renamed "value"

        // "int" is a reserved keyword, cannot be used as a variable name
        int integerValue = 100000; // Removed "int"

        int myInteger = 1000000;   // Removed special characters in "my integer"

        // Invalid variable name with operators: "my+var"
        // You cannot use operators in variable names.

        int myMinusMinusInteger = 1234567; // Removed special characters in "my__-__integer"
    }
}
